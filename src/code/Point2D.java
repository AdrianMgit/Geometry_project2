package code;

public class Point2D {
    private double x;
    private double y;

    public Point2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    void wypisz() {
        System.out.println("Wspolrzedna x punktu wynosi: "+x);
        System.out.println("Wspolrzedna y punktu wynosi: "+y);
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

}



























